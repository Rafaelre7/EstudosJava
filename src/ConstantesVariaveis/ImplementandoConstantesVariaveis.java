package ConstantesVariaveis;

public class ImplementandoConstantesVariaveis {
	
	static String nome = "Rafael"; // Valor pode ser alterado
	final static  String SOBRENOME = "Pimenta"; //Valor não pode ser alterado
	
	public static void main(String[] args) {
		
			
		System.out.println("Imprimindo nome: "+nome); 
		System.out.println("Imprimindo constante: "+SOBRENOME);
		
	}

}
